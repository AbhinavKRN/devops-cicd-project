package com.devops.taskmanager.model;

/**
 * Enum representing task priority levels.
 */
public enum TaskPriority {
    /**
     * Low priority task.
     */
    LOW,

    /**
     * Medium priority task.
     */
    MEDIUM,

    /**
     * High priority task.
     */
    HIGH,

    /**
     * Critical priority task requiring immediate attention.
     */
    CRITICAL
}

